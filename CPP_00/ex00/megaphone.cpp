#include <iostream>

int main(int argc, char **argv)
{
    if (argc == 1)
        std::cout << "* LOUD AND UNBEARABLE FEEDBACK NOISE *" << std::endl;
    else
    {
        for (int i = 1; i < argc; i++)
        {
            std::string str = argv[i];
            for (size_t j = 0; j < str.length(); j++)
            {
                unsigned char ch = str[j];
                ch = toupper(ch);
                std::cout << ch;
            }
        }
        std::cout << std::endl;
    }
    return (0);
}